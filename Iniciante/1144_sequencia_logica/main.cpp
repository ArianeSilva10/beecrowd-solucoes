#include <iostream>
using namespace std;

int main(){
    int n,  toggle = 0;
    cin >> n;
    
    for (int i = 1; i <= n; i++)
    {
        for (int j = 0; j < 2; j++)
        {
            cout << i << " " << i*i + toggle << " " << i*i*i + toggle << endl;
            toggle = toggle == 0 ? 1 : 0;
        }
    }
    
}



